# Arithmetic Operators
a = 10
b = 5
print(f'Arithmetic Operators:')
print(f'a + b = {a + b}')  # Addition
print(f'a - b = {a - b}')  # Subtraction
print(f'a * b = {a * b}')  # Multiplication
print(f'a / b = {a / b}')  # Division
print(f'a % b = {a % b}')  # Modulus
print(f'a ** b = {a ** b}')  # Exponentiation
print(f'a // b = {a // b}')  # Floor division

# Comparison Operators
print(f'\nComparison Operators:')
print(f'a == b: {a == b}')  # Equal to
print(f'a != b: {a != b}')  # Not equal to
print(f'a > b: {a > b}')    # Greater than
print(f'a < b: {a < b}')    # Less than
print(f'a >= b: {a >= b}')  # Greater than or equal to
print(f'a <= b: {a <= b}')  # Less than or equal to

# Logical Operators
x = True
y = False
print(f'\nLogical Operators:')
print(f'x and y: {x and y}')  # Logical AND
print(f'x or y: {x or y}')    # Logical OR
print(f'not x: {not x}')      # Logical NOT

# Assignment Operators
a = 10
a += 5
print(f'\nAssignment Operators:')
print(f'a += 5: {a}')  # a = a + 5
a -= 5
print(f'a -= 5: {a}')  # a = a - 5
a *= 2
print(f'a *= 2: {a}')  # a = a * 2
a /= 2
print(f'a /= 2: {a}')  # a = a / 2
a %= 5
print(f'a %= 5: {a}')  # a = a % 5

# Bitwise Operators
a = 10
b = 5
print(f'\nBitwise Operators:')
print(f'a & b: {a & b}')  # AND
print(f'a | b: {a | b}')  # OR
print(f'a ^ b: {a ^ b}')  # XOR
print(f'~a: {~a}')        # NOT
print(f'a << 1: {a << 1}')  # Left shift
print(f'a >> 1: {a >> 1}')  # Right shift
